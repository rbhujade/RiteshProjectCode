namespace MathFunction
{
    public class BasicMath
    {
        public double Add(double x,double y)
        {
            return x + y;
        }

        public double Sub(double x, double y)
        {
            return x - y;
        }
        public double Divide(double x, double y)
        {
            return x / y;
        }
        public double Multi(double x, double y)
        {
            return x + y;
        }
    }
}
